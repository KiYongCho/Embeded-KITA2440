-- 5/20 SQL 실습 2 : 집합연산(합집합, 교집합, 차집합)

CREATE TABLE SETTEST1 (
	SNAME VARCHAR2(20),
	SAGE NUMBER
);

CREATE TABLE SETTEST2 (
	SNAME VARCHAR2(20),
	SAGE NUMBER
);

INSERT INTO SETTEST1 VALUES('홍길동', 20);
INSERT INTO SETTEST1 VALUES('강감찬', 30);
INSERT INTO SETTEST1 VALUES('이순신', 40);

INSERT INTO SETTEST2 VALUES('홍길동', 20);
INSERT INTO SETTEST2 VALUES('강감찬', 30);
INSERT INTO SETTEST2 VALUES('장보고', 50);

COMMIT

SELECT * FROM SETTEST1;
SELECT * FROM SETTEST2;

-- 합집합
SELECT * FROM SETTEST1
UNION
SELECT * FROM SETTEST2;

-- 합집합 + 교집합
SELECT * FROM SETTEST1
UNION ALL
SELECT * FROM SETTEST2;

-- 교집합
SELECT * FROM SETTEST1
INTERSECT
SELECT * FROM SETTEST2;

-- 차집합
SELECT * FROM SETTEST1
MINUS
SELECT * FROM SETTEST2;

-- 차집합
SELECT * FROM SETTEST2
MINUS
SELECT * FROM SETTEST1;












